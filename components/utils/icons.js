/**
 * Get an icon by name
 * @param {string} name - Icon name
 * @param {Object} props - Optional props (size, class, etc.)
 * @returns {string} SVG string with applied props
 */
export const getIcon = (name, props = {}) => {
    const svg = icons[name];
    if (!svg) {
        console.warn(`Icon "${name}" not found`);
        return '';
    }
    
    let result = svg;
    
    // Apply size
    if (props.size) {
        result = result
            .replace(/width="24"/, `width="${props.size}"`)
            .replace(/height="24"/, `height="${props.size}"`);
    }
    
    // Apply class
    if (props.class) {
        result = result.replace('<svg', `<svg class="${props.class}"`);
    }
    
    return result;
};

/**
 * Create an icon element (for use with Lightview)
 * @param {string} name - Icon name
 * @param {Object} props - Props including size, class
 * @returns {Object} vDOM-compatible object
 */
export const Icon = (name, props = {}) => {
    const { tags } = window.Lightview || {};
    if (!tags) {
        console.warn('Lightview not found');
        return null;
    }
    
    const { span } = tags;
    const { size = 24, class: className = '', ...rest } = props;
    
    return span({
        class: `lv-icon ${className}`.trim(),
        innerHTML: getIcon(name, { size }),
        ...rest
    });
};

